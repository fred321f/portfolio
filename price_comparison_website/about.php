      <h3 class="display-6">Hvordan finder man sit SIM-kortnummer?</h3>
      <p class="lead">
        Hvis man ikke helt har styr på mobiltelefoni, kan det godt være svært at finde ting, som foreksempel sit SIM-kortnummer. Derfor vil vi gøre vores bedste for,
        at du kommer hurtigt og smertefrit igennem det, så du slipper for frustrationer.
        <br><br>
        <strong>Find dit SIM-kortnummer på iOS-enheder:</strong>
        <br>
        På iPhone eller iPad skal du gå ind under indstillinger. Herfra bladrer du lidt ned og trykker på <strong>Generelt</strong>, og herefter på <strong>Om</strong>.
        I rækken med <strong>ICCID</strong> finder du dit SIM-kortnummer.
        <br><br>
        <strong>Find SIM-kortnummer på Android:</strong>
        <br>
        På android-telefoner kan du desværre ikke gøre på samme måde som på iOS-enheder. Du kan derimod gå ind og installere <strong>en app kaldet "SIM-kort info" via Google Play-butikken</strong> på din enhed.
        I denne app finder du dit SIM-kortnummer, hvis du har en android-telefon.
        Vær dog opmærksom på, at den ikke er helt fungerende med alle android-enheder. Hvis du kommer ud for, at app'en ikke virker til din enhed,
        er du desværre nødt til at tage SIM-kortet ud af din telefon, for at finde dit SIM-kortnummer. Nummeret står printet på kortet.
        <br><br>
        <strong>Find SIM-kortnummer på Windows-telefoner:</strong>
        <br>
        På windows-telefoner gælder næsten det samme som på iPhones og iPads. På operativsystem-version 7.5 eller højere, skal du trykke på <strong>pil-ikonet</strong>, herefter <strong>indstillinger</strong>, på <strong>om</strong> og til sidst på <strong>mere info</strong>. Her finder du ud for <strong>SIM ID</strong> dit SIM-kortnummer.

